void Config::displayWebPage(){
  //start the Server
  server.on("/", [&]() {
      String date = Config::printLocalTime();
      server.send(200, "text/html", sendData(date));
  });
  server.onNotFound([]() {
      server.send(404, "text/plain", "Not found");
  }); 
  server.begin();
  Serial.println("HTTP server started");        
}

void Config::startClient(){
server.handleClient();
}

