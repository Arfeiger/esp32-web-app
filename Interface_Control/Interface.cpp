String Config::printLocalTime(){
  struct tm timeinfo;
  if(!getLocalTime(&timeinfo)){
      Serial.println("Failed to obtain time");
  }
  //Converts the given date and time to string 
  char date_string[100];
  strftime(date_string, 50, "%A, %B %d %Y %H:%M:%S", &timeinfo);
  Serial.println(date_string);
  return date_string ;
}

